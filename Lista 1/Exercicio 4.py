# 4. Faça um algoritmo que calcule a área de um Triângulo (os valores da base e altura devem ser lidos do teclado)

base = float(input("Digite o tamanho da base do triângulo: "))
altura = float(input("Digite o tamanho da altura do triângulo: "))

area = (base * altura) / 2
print("A área do triângulo é:", area)